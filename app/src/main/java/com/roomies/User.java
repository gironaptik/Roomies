package com.roomies;

public class User {
    public String id;
    public String name;
    public String email;
    public String image;

    public User(){

    }

    public String getImage() {
        return image;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}
